            $goods_properties[$key]['spec_id'] = $val['spec_id'];
            $goods_properties[$key]['template_pid'] = '';
            $goods_properties[$key]['value'] = $val['vvalue'];//属性值
            $goods_properties[$key]['value_unit'] = '';//属性单位
            $goods_properties[$key]['vid'] = $val['vid'];//属性值id
        }
