        $result = $result['goods_spec_get_response']['goods_spec_list'];
        $specid = '';
        foreach ($result as $item) {
            if($item['parent_spec_name'] == $spec_cate){
                $specid = $item['parent_spec_id'] ;
            }
        }
        $spec_ids =  $this -> getpddskuSpecsId($specid,$specname);
        return $spec_ids;
    }

